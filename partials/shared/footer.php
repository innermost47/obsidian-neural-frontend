<script>
  (function() {
    var cfg = window.APP_CONFIG || {};
    var siteName = cfg.SITE_NAME || "OBSIDIAN Neural";
    var githubUrl = cfg.GITHUB_URL || "#";
    var githubIssues =
      cfg.GITHUB_ISSUES_URL ||
      (githubUrl !== "#" ? githubUrl + "/issues" : "#");
    var githubDiscuss = githubUrl !== "#" ? githubUrl + "/discussions" : "#";
    var githubLicense =
      githubUrl !== "#" ? githubUrl + "/blob/main/LICENSE" : "#";
    var kvraudioUrl = cfg.KVRAUDIO_URL || "#";
    var mediumUrl = cfg.MEDIUM_URL || "#";
    var youtubeUrl = cfg.YOUTUBE_URL || "#";
    var paperUrl = cfg.PAPER_URL || "#";
    var authorUrl = cfg.AUTHOR_URL || "#";
    var authorAlias = cfg.AUTHOR_ALIAS || "InnerMost47";

    function setHref(id, url) {
      var el = document.getElementById(id);
      if (el && url && url !== "#") el.href = url;
    }

    function setText(id, val) {
      var el = document.getElementById(id);
      if (el) el.textContent = val;
    }

    setText("footer-site-name", siteName);
    setText("footer-brand-name", siteName);

    setHref("footer-github", githubUrl);
    setHref("footer-medium", mediumUrl);
    setHref("footer-youtube", youtubeUrl);
    setHref("footer-kvr", kvraudioUrl);

    setHref(
      "footer-download",
      githubUrl !== "#" ? githubUrl + "/releases/latest" : "#",
    );

    setHref("footer-github-res", githubUrl);
    setHref("footer-kvr-res", kvraudioUrl);
    setHref("footer-blog", mediumUrl);
    setHref("footer-paper", paperUrl);

    setHref("footer-about", authorUrl);
    setHref("footer-support", githubIssues);
    setHref("footer-community", githubDiscuss);

    setHref("footer-license", githubLicense);

    setText("footer-year", new Date().getFullYear());
    var authorEl = document.getElementById("footer-author-link");
    if (authorEl) {
      authorEl.href =
        "https://github.com/" +
        (cfg.GITHUB_USERNAME || authorAlias.toLowerCase());
      authorEl.textContent = authorAlias;
    }
  })();
</script>
